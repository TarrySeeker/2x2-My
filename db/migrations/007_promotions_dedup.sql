-- ============================================================
-- 007_promotions_dedup.sql — дедупликация акций по title
-- ============================================================
-- Клиентская правка 2026-04-24: на проде появились дубли «Замер и
-- фотомонтаж — бесплатно» после повторных накатов seed_cms.sql.
--
-- Этой миграцией:
--   1. Удаляем дубликаты (оставляем минимальный id на каждый title).
--   2. Ставим уникальный индекс по title, чтобы повторные вставки
--      сработали в ON CONFLICT DO NOTHING в seed_cms.sql.
-- ============================================================

BEGIN;

-- 1. Удаляем дубли (оставляем самую раннюю запись по id).
DELETE FROM promotions a
USING promotions b
WHERE a.title = b.title
  AND a.id    > b.id;

-- 2. Уникальный индекс — предотвращает новые дубли.
CREATE UNIQUE INDEX IF NOT EXISTS idx_promotions_title_unique
  ON promotions (title);

COMMIT;
